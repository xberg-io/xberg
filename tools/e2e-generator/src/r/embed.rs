use super::render::{render_numeric_literal, render_r_string, render_string_vector};
use crate::fixtures::Fixture;
use anyhow::{Context, Result};
use camino::Utf8Path;
use serde_json::{Map, Value};
use std::fmt::Write as _;
use std::fs;

pub fn generate_embed_tests(fixtures: &[Fixture], test_dir: &Utf8Path) -> Result<()> {
    let embed_fixtures: Vec<_> = fixtures.iter().filter(|f| f.is_embed()).collect();
    if embed_fixtures.is_empty() {
        return Ok(());
    }

    let mut buffer = String::new();
    writeln!(buffer, "# Code generated by kreuzberg-e2e-generator. DO NOT EDIT.")?;
    writeln!(
        buffer,
        "# To regenerate: cargo run -p kreuzberg-e2e-generator -- generate --lang r"
    )?;
    writeln!(buffer, "# nolint start")?;
    writeln!(buffer)?;

    for fixture in embed_fixtures {
        buffer.push_str(&render_embed_test(fixture)?);
    }

    writeln!(buffer, "# nolint end")?;
    let path = test_dir.join("test-embeddings.R");
    fs::write(&path, buffer).with_context(|| format!("Writing {}", path))?;

    Ok(())
}

fn render_embed_test(fixture: &Fixture) -> Result<String> {
    let mut buffer = String::new();
    let embed = fixture.embed.as_ref().expect("embed spec required");
    let fixture_assertions = fixture.assertions();
    let assertions = fixture_assertions.embed.as_ref().expect("embed assertions required");

    writeln!(buffer, "test_that({}, {{", render_r_string(&fixture.id))?;

    if !fixture.id.contains("disabled") {
        writeln!(buffer, "  if (.Platform$OS.type == \"windows\" && .Platform$r_arch == \"x64\") {{")?;
        writeln!(buffer, "    testthat::skip(\"Skip embeddings on Windows X64 until ONNX is implemented\")")?;
        writeln!(buffer, "  }}")?;
    }

    let texts_vector = render_string_vector(&embed.texts);
    let config_expr = if !embed.config.is_empty() {
        render_embed_config_r(&embed.config)?
    } else {
        "NULL".to_string()
    };

    let func = if embed.async_variant {
        "embed_texts"
    } else {
        "embed_texts_sync"
    };

    writeln!(buffer, "  results <- tryCatch(")?;
    writeln!(
        buffer,
        "    {}({}, config = {}),",
        func, texts_vector, config_expr
    )?;
    writeln!(buffer, "    error = function(e) {{")?;
    writeln!(
        buffer,
        "      testthat::fail(paste0(\"Embedding failed: \", conditionMessage(e)))"
    )?;
    writeln!(buffer, "    }}")?;
    writeln!(buffer, "  )")?;

    let count = assertions.count.map(|c| c as i32).unwrap_or(-1);
    let dimensions = assertions.dimensions.map(|d| d as i32).unwrap_or(-1);

    writeln!(
        buffer,
        "  assert_embed_result(results, {}, {}, {}, {}, {})",
        count,
        dimensions,
        if assertions.no_nan { "TRUE" } else { "FALSE" },
        if assertions.no_inf { "TRUE" } else { "FALSE" },
        if assertions.non_zero { "TRUE" } else { "FALSE" }
    )?;

    writeln!(buffer, "}})")?;
    writeln!(buffer)?;

    Ok(buffer)
}

fn render_embed_config_r(config: &Map<String, Value>) -> Result<String> {
    let mut parts = Vec::new();

    if let Some(model) = config.get("model") {
        if let Some(name) = model.get("name").and_then(|v| v.as_str()) {
            parts.push(format!("model = list(name = \"{}\")", name));
        }
    }

    if let Some(v) = config.get("normalize").and_then(|v| v.as_bool()) {
        parts.push(format!("normalize = {}", if v { "TRUE" } else { "FALSE" }));
    }

    if let Some(v) = config.get("batch_size").and_then(|v| v.as_i64()) {
        parts.push(format!("batch_size = {}L", v));
    }

    if parts.is_empty() {
        Ok("NULL".to_string())
    } else {
        Ok(format!("list({})", parts.join(", ")))
    }
}
