pub fn generate_parity(manifest: &ParityManifest, output_root: &Utf8Path) -> Result<()> {
    let r_root = output_root.join("r");
    let test_dir = r_root.join("tests").join("testthat");
    fs::create_dir_all(&test_dir).context("Failed to create R test directory")?;

    let lang = "r";
    let mut buf = String::new();

    writeln!(buf, "# Code generated by kreuzberg-e2e-generator. DO NOT EDIT.")?;
    writeln!(
        buf,
        "# To regenerate: cargo run -p kreuzberg-e2e-generator -- generate --lang r"
    )?;
    writeln!(buf, "#")?;
    writeln!(
        buf,
        "# Parity tests: verify ExtractionResult and ExtractionConfig have all expected fields."
    )?;
    writeln!(buf, "# nolint start")?;
    writeln!(buf)?;

    // ExtractionResult field parity
    if let Some(fields) = parity::fields_for_type_and_lang(manifest, "ExtractionResult", lang) {
        let field_names: Vec<&String> = fields.keys().collect();

        writeln!(buf, "test_that(\"ExtractionResult has all expected fields\", {{")?;
        writeln!(
            buf,
            "  result <- kreuzberg::extract_bytes(charToRaw(\"hello\"), \"text/plain\")"
        )?;
        writeln!(buf, "  expected_fields <- c(")?;
        for (i, field) in field_names.iter().enumerate() {
            let comma = if i < field_names.len() - 1 { "," } else { "" };
            writeln!(buf, "    \"{field}\"{comma}")?;
        }
        writeln!(buf, "  )")?;
        writeln!(buf, "  actual_fields <- names(result)")?;
        writeln!(buf, "  for (field in expected_fields) {{")?;
        writeln!(
            buf,
            "    expect_true(field %in% actual_fields, info = paste(\"missing field:\", field))"
        )?;
        writeln!(buf, "  }}")?;
        writeln!(buf, "}})")?;
        writeln!(buf)?;
    }

    // ExtractionConfig field parity
    if let Some(fields) = parity::fields_for_type_and_lang(manifest, "ExtractionConfig", lang) {
        let field_names: Vec<&String> = fields.keys().collect();

        writeln!(buf, "test_that(\"ExtractionConfig has all expected fields\", {{")?;
        writeln!(buf, "  expected_fields <- c(")?;
        for (i, field) in field_names.iter().enumerate() {
            let comma = if i < field_names.len() - 1 { "," } else { "" };
            writeln!(buf, "    \"{field}\"{comma}")?;
        }
        writeln!(buf, "  )")?;
        writeln!(
            buf,
            "  expect_true(length(expected_fields) > 0, info = \"should have at least one config field\")"
        )?;
        writeln!(buf, "}})")?;
        writeln!(buf)?;
    }

    // Other struct types
    for (type_name, type_def) in &manifest.types {
        if *type_name == "ExtractionResult" || *type_name == "ExtractionConfig" {
            continue;
        }
        if let TypeDef::Struct { .. } = type_def
            && let Some(fields) = parity::fields_for_type_and_lang(manifest, type_name, lang)
        {
            let field_names: Vec<&String> = fields.keys().collect();

            writeln!(buf, "test_that(\"{type_name} has all expected fields\", {{")?;
            writeln!(buf, "  expected_fields <- c(")?;
            for (i, field) in field_names.iter().enumerate() {
                let comma = if i < field_names.len() - 1 { "," } else { "" };
                writeln!(buf, "    \"{field}\"{comma}")?;
            }
            writeln!(buf, "  )")?;
            writeln!(
                buf,
                "  expect_true(length(expected_fields) > 0, info = \"{type_name} should have at least one field\")"
            )?;
            writeln!(buf, "}})")?;
            writeln!(buf)?;
        }
    }

    writeln!(buf, "# nolint end")?;

    let path = test_dir.join("test-parity.R");
    fs::write(&path, &buf).with_context(|| format!("Writing {path}"))?;

    Ok(())
}
