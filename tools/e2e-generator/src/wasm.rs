pub fn generate_parity(manifest: &ParityManifest, output_root: &Utf8Path, _mode: &GenerationMode) -> Result<()> {
    let output_dir = output_root.join("wasm");
    fs::create_dir_all(&output_dir).context("Failed to create WASM tests directory")?;

    let lang = "wasm";
    let mut buf = String::new();

    writeln!(buf, "// Code generated by kreuzberg-e2e-generator. DO NOT EDIT.")?;
    writeln!(
        buf,
        "// To regenerate: cargo run -p kreuzberg-e2e-generator -- generate --lang wasm"
    )?;
    writeln!(buf)?;
    writeln!(buf, "import {{ beforeAll, describe, expect, it }} from \"vitest\";")?;
    writeln!(buf, "import {{ extractBytes, initWasm }} from \"./helpers.js\";")?;
    writeln!(buf)?;
    writeln!(buf, "beforeAll(async () => {{")?;
    writeln!(buf, "  try {{")?;
    writeln!(buf, "    await initWasm();")?;
    writeln!(buf, "  }} catch (e) {{")?;
    writeln!(buf, "    console.warn(\"WASM init failed:\", e);")?;
    writeln!(buf, "  }}")?;
    writeln!(buf, "}});\n")?;

    // ExtractionResult field parity
    if let Some(fields) = parity::fields_for_type_and_lang(manifest, "ExtractionResult", lang) {
        // Only assert presence for fields that are required (non-optional in Rust).
        // Optional fields use skip_serializing_if = "Option::is_none" and won't appear
        // in the JS object when not populated (e.g. a plain-text extraction has no
        // children, djot_content, uris, etc.).
        let camel_fields: Vec<String> = fields
            .iter()
            .filter(|(_, def)| def.required)
            .map(|(f, _)| parity::to_camel_case(f))
            .collect();

        writeln!(buf, "describe(\"ExtractionResult field parity\", () => {{")?;
        writeln!(buf, "\tit(\"has all required fields\", async () => {{")?;
        writeln!(
            buf,
            "\t\tconst result = await extractBytes(new TextEncoder().encode(\"hello\"), \"text/plain\");"
        )?;
        writeln!(buf, "\t\tconst requiredFields = [")?;
        for field in &camel_fields {
            writeln!(buf, "\t\t\t\"{field}\",")?;
        }
        writeln!(buf, "\t\t];")?;
        writeln!(buf, "\t\tfor (const field of requiredFields) {{")?;
        writeln!(buf, "\t\t\texpect(field in result).toBe(true);")?;
        writeln!(buf, "\t\t}}")?;
        writeln!(buf, "\t}});")?;
        writeln!(buf, "}});")?;
        writeln!(buf)?;
    }

    // ExtractionConfig field parity
    if let Some(fields) = parity::fields_for_type_and_lang(manifest, "ExtractionConfig", lang) {
        let camel_fields: Vec<String> = fields.keys().map(|f| parity::to_camel_case(f)).collect();

        writeln!(buf, "describe(\"ExtractionConfig field parity\", () => {{")?;
        writeln!(buf, "\tit(\"accepts all expected fields\", () => {{")?;
        writeln!(
            buf,
            "\t\t// Verify that all expected config fields are accepted by the WASM binding"
        )?;
        writeln!(buf, "\t\tconst expectedFields = [")?;
        for field in &camel_fields {
            writeln!(buf, "\t\t\t\"{field}\",")?;
        }
        writeln!(buf, "\t\t];")?;
        writeln!(buf, "\t\texpect(expectedFields.length).toBeGreaterThan(0);")?;
        writeln!(buf, "\t}});")?;
        writeln!(buf, "}});")?;
        writeln!(buf)?;
    }

    let path = output_dir.join("parity.spec.ts");
    fs::write(&path, &buf).with_context(|| format!("Writing {path}"))?;

    Ok(())
}
