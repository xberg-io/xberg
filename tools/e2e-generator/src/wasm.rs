fn render_embed_test_deno(fixture: &Fixture) -> Result<String> {
    let mut buffer = String::new();
    let embed = fixture.embed.as_ref().expect("embed spec required");
    let fixture_assertions = fixture.assertions();
    let assertions = fixture_assertions.embed.as_ref().expect("embed assertions required");

    let title = format!("{} - {}", to_title_case(fixture.category()), fixture.id);

    writeln!(buffer, "Deno.test(\"{}\", async () => {{", escape_ts_string(&title))?;
    writeln!(buffer, "    await initWasm();")?;

    let texts_json = serde_json::to_string(&embed.texts)?;
    let config_json = serde_json::to_string(&embed.config)?;

    writeln!(buffer, "    const texts = {};", texts_json)?;
    writeln!(buffer, "    const config = buildConfig({});", config_json)?;

    writeln!(buffer, "    const results = await embedTexts(texts, config);")?;

    let count = assertions.count.map(|c| c as i32).unwrap_or(-1);
    let dimensions = assertions.dimensions.map(|d| d as i32).unwrap_or(-1);

    writeln!(
        buffer,
        "    assertions.assertEmbedResult(results, {}, {}, {}, {}, {});",
        count,
        dimensions,
        if assertions.no_nan { "true" } else { "false" },
        if assertions.no_inf { "true" } else { "false" },
        if assertions.non_zero { "true" } else { "false" }
    )?;

    writeln!(buffer, "}});")?;
    writeln!(buffer)?;

    Ok(buffer)
}

